package akingspackage;

public class ForLoops {

	public static void main(String[] args) {
		// initialize variable, check condition, increment value
		// Set ForLoops to run command until 10 is reached
		for(int t=10; t>=0; t--) {
			//Should print out "Im still hungry plus t
			System.out.println("Im still hungry "+  t);
		}
	
	}
	}	


